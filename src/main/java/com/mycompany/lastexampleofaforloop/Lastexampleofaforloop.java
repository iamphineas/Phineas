public class Lastexampleofaforloop {

    public static void main(String[] args) {
        int max = 9; 
                for( int i=0; i<=max;i++) {
                    for(int j=0;j<=max;j++) {
                        System.out.printf("[%d,%d]",i,j);  
                    
          }
                    System.out.println();       
       }
    }
}
